#10. Write a program to display the type of converted variable
num = 25
con_str = str(num)
print("number :",num)

print("Convert string:", con_str)
print("type",type(con_str))

con_float = float(num)
print("Convert float:", con_float)
print("type",type(con_float))

con_integer = int(num)
print("Convert Integer:", con_integer)
print("type",type(con_integer))
